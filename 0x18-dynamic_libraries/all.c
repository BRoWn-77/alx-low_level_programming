#include <stdio.h>

/**
 * add - add two integers
 * @a: first int
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * div - division of two integers
 * @a: first int
 * @b: second int
 * Return: answer
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mul - mul of two integers
 * @a: first int
 * @b: second int
 * Return: multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - mod of two integers
 * @a: first int
 * @b: second int
 * Return: answer
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * sub - subtract two integers
 * @a: first int
 * @b: second int
 * Return: subtracted final results
 */
int sub(int a, int b)
{
	return (a - b);
}
